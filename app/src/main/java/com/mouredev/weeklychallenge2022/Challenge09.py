def Morse(txt):
    
    abc2morse={
        "A":".—", "N":"—.", "0":"—————", 
        "B":"—...", "Ñ":"——.——", "1":".————", 
        "C":"—.—.", "O":"———", "2":"..———", 
        "CH":"————", "P":".——.", "3":"...——", 
        "D":"—..", "Q":"——.—", "4":"....—", 
        "E":".", "R":".—.", "5":".....", 
        "F":"..—.", "S":"...", "6":"—....", 
        "G":"——.", "T":"—", "7":"——...", 
        "H":"....", "U":"..—", "8":"———..", 
        "I":"..", "V":"...—", "9":"————.", 
        "J":".———", "W":".——", ".":".—.—.—", 
        "K":"—.—", "X":"—..—", ", ":"——..——", 
        "L":".—..", "Y":"—.——", "?":"..——..", 
        "M":"——", "Z":"——..", "\"":".—..—.", 
        "/":"—..—.", " ":" "
    }
    
    morse2abc={
        ".—":"A", "—.":"N", "—————":"0", 
        "—...":"B", "——.——":"Ñ", ".————":"1", 
        "—.—.":"C", "———":"O", "..———":"2", 
        "————":"CH", ".——.":"P", "...——":"3", 
        "—..":"D", "——.—":"Q", "....—":"4", 
        ".":"E", ".—.":"R", ".....":"5", 
        "..—.":"F", "...":"S", "—....":"6", 
        "——.":"G", "—":"T", "——...":"7", 
        "....":"H", "..—":"U", "———..":"8", 
        "..":"I", "...—":"V", "————.":"9", 
        ".———":"J", ".——":"W", ".—.—.—":".", 
        "—.—":"K", "—..—":"X", "——..——":",", 
        ".—..":"L", "—.——":"Y", "..——..":"?", 
        "——":"M", "——..":"Z", ".—..—.":"\"", 
        "—..—.":"/", " ":" "
    }
    
    newTxt="Texto original: "+txt+"\nTexto Nuevo: "
    
    txt=txt.upper()
    
    countM=0
    countA=0
    
    for char in txt:
        if char not in morse2abc and char not in abc2morse:
            print("Error en el caracter: ",char)
            return ""
        else:
            if char != " " and char != ".":
                if char in abc2morse:
                    countA+=1
                elif char in morse2abc:
                    countM+=1
    if (countM>0 and countA>0):
        print("countM",countM)
        print("countA",countA)
        print("Error, no puede determinarse si el mensaje esta en lenguaje natural o morse")
        return ""
    elif(countA>0):
        for char in txt:
            newTxt+=abc2morse[char]
            newTxt+=" "
    else:
        for char in txt.split():
            newTxt+=morse2abc[char]
            newTxt+=" "
    newTxt+="\n"    
    

    return newTxt
                
